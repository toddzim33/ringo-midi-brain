def lighting_kill_unblackout(qlc):
    qlc_kill(qlc)
    qlc_unblackout(qlc)


def process_note_on(note, velocity, qlc):
    global ga, gb, blackout_active

    if note == NOTE_PREV_SCENE:
        lighting_kill_unblackout(qlc)
        ga = (ga - 1) % 10
        qq = current_ls + ga
        qlc_fire(qlc, qq)
        log.info(f"PREV SCENE -> note {qq}")

    elif note == NOTE_NEXT_SCENE:
        lighting_kill_unblackout(qlc)
        ga = (ga + 1) % 10
        qq = current_ls + ga
        qlc_fire(qlc, qq)
        log.info(f"NEXT SCENE -> note {qq}")

    elif note == NOTE_SCENE_CHORD:
        lighting_kill_unblackout(qlc)
        qq = QLC_NOTE_BASE_SCENE if velocity > VELOCITY_HARD else QLC_NOTE_ALL_SCENE_CHASER
        qlc_fire(qlc, qq)
        log.info(f"SCENE CHORD vel={velocity} -> note {qq}")

    elif note == NOTE_CHASE_CHORD:
        lighting_kill_unblackout(qlc)
        qq = QLC_NOTE_STROBE if velocity > VELOCITY_HARD else QLC_NOTE_ALL_CHASE_CHASER
        qlc_fire(qlc, qq)
        log.info(f"CHASE CHORD vel={velocity} -> note {qq}")

    elif note == NOTE_PREV_CHASE:
        lighting_kill_unblackout(qlc)
        gb = (gb - 1) % 10
        qq = current_lc + gb
        qlc_fire(qlc, qq)
        log.info(f"PREV CHASE -> note {qq}")

    elif note == NOTE_NEXT_CHASE:
        lighting_kill_unblackout(qlc)
        gb = (gb + 1) % 10
        qq = current_lc + gb
        qlc_fire(qlc, qq)
        log.info(f"NEXT CHASE -> note {qq}")

    elif note == NOTE_BLACKOUT:
        blackout_active = not blackout_active
        if blackout_active:
            qlc_blackout_dmx()
        else:
            qlc_fire_dmx(current_ls + ga)
        log.info(f"BLACKOUT {'ON' if blackout_active else 'OFF'}")


