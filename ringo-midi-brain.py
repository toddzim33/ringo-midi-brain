_qlc_ws = None
_qlc_lock = threading.Lock()
_qlc_dmx_state = {}  # ch_str -> val, tracks what's currently lit

def _qlc_connect():
    """Open a fresh QLC+ WebSocket and configure IO. Returns ws or None."""
    import websocket as _wsmod
    try:
        ws = _wsmod.create_connection("ws://localhost:9999/qlcplusWS", timeout=5)
        ws.send("QLC+IO|INPUT|0|MIDI|1")
        ws.send("QLC+IO|OUTPUT|0|DMX USB|0")
        log.info("QLC+ WebSocket connected, IO configured")
        return ws
    except Exception as e:
        log.warning(f"QLC+ WS connect failed: {e}")
        return None

def _qlc_send(messages):
    """Send messages over persistent QLC+ WS; reconnects once on failure."""
    global _qlc_ws
    with _qlc_lock:
        for attempt in range(2):
            if _qlc_ws is None:
                _qlc_ws = _qlc_connect()
            if _qlc_ws is None:
                return
            try:
                for msg in messages:
                    _qlc_ws.send(msg)
                return
            except Exception as e:
                log.warning(f"QLC+ WS send failed (attempt {attempt+1}): {e}")
                try:
                    _qlc_ws.close()
                except Exception:
                    pass
                _qlc_ws = None

_KILL_NOTES = {68, 89}  # QLC+ "Kill Functions" buttons


def qlc_fire_dmx(note):
    """Set DMX scene by note. Only sends channels that changed from current state."""
    global _qlc_dmx_state
    if note in _KILL_NOTES:
        qlc_stop_chaser()
        qlc_blackout_dmx()
        log.info(f"qlc_fire_dmx: note {note} -> kill/blackout")
        return
    scene = SCENE_DMX.get(note, {})
    if not scene:
        log.warning(f"qlc_fire_dmx: no DMX data for note {note}")
        return
    # Zero channels that were lit but aren't in new scene
    to_zero = {ch: 0 for ch in _qlc_dmx_state if ch not in scene}
    msgs = [f"CH|{ch}|0" for ch in to_zero]
    msgs += [f"CH|{ch}|{val}" for ch, val in scene.items()]
    _qlc_send(msgs)
    _qlc_dmx_state = dict(scene)
    log.info(f"qlc_fire_dmx: note {note} -> {len(scene)} lit, {len(to_zero)} zeroed")

def qlc_blackout_dmx():
    """Zero all currently-lit DMX channels."""
    global _qlc_dmx_state
    if not _qlc_dmx_state:
        return
    msgs = [f"CH|{ch}|0" for ch in _qlc_dmx_state]
    _qlc_send(msgs)
    _qlc_dmx_state = {}

def qlc_kill(qlc):
    qlc_stop_chaser()

def qlc_unblackout(qlc):
    global blackout_active
    if blackout_active:
        qlc_fire_dmx(current_ls + ga)
        blackout_active = False
        log.info("blackout released")

def qlc_fire(qlc, note):
    qlc_fire_dmx(note)
    qlc_start_chaser(note)  # no-op if note has no animated sequence

def qlc_stop_chaser():
    """Signal any running chaser thread to stop and wait for it to exit."""
    global _chaser_thread
    _chaser_stop.set()
    if _chaser_thread is not None and _chaser_thread.is_alive():
        _chaser_thread.join(timeout=0.5)
    _chaser_thread = None


def qlc_start_chaser(note):
    """Start an animated chaser loop for note (no-op if note has no chaser data)."""
    global _chaser_thread, _chaser_stop, _qlc_dmx_state
    steps = CHASER_DATA.get(note)
    if not steps:
        return
    qlc_stop_chaser()
    _chaser_stop = threading.Event()
    stop = _chaser_stop

    def _run():
        global _qlc_dmx_state
        while not stop.is_set():
            for dur_ms, channels in steps:
                if stop.is_set():
                    break
                msgs = [f"CH|{ch}|{val}" for ch, val in channels.items()]
                _qlc_send(msgs)
                _qlc_dmx_state = dict(channels)
                stop.wait(dur_ms / 1000.0)

    _chaser_thread = threading.Thread(target=_run, daemon=True,
                                      name=f'chaser-{note}')
    _chaser_thread.start()
    log.info(f"chaser started: note {note}, {len(steps)} steps")


def qlc_fire_scene_and_chase(ls, lc):
    """Stop current chaser, fire combined static ls+lc, then start animated ls chaser."""
    global _qlc_dmx_state
    qlc_stop_chaser()
    combined = dict(SCENE_DMX.get(ls, {}))
    combined.update(SCENE_DMX.get(lc, {}))  # lc overlays ls
    if not combined:
        log.warning(f"qlc_fire_scene_and_chase: no DMX data for ls={ls} lc={lc}")
        return
    to_zero = {ch: 0 for ch in _qlc_dmx_state if ch not in combined}
    msgs = [f"CH|{ch}|0" for ch in to_zero]
    msgs += [f"CH|{ch}|{val}" for ch, val in combined.items()]
    _qlc_send(msgs)
    _qlc_dmx_state = dict(combined)
    log.info(f"qlc_fire_scene_and_chase: ls={ls} lc={lc} -> {len(combined)} lit, {len(to_zero)} zeroed")
    qlc_start_chaser(ls)  # animated ls sequence starts (no-op if ls is static)

def qlc_stomp_mirror(qlc, rr):
    """Mirror Stomp PC + CC 69 value=0 (100ms delay) to QLC+ -- matches Bome Outgoing2/3."""
    send_pc(qlc, 1, rr)
    def _cc():
        time.sleep(0.100)
        send_cc(qlc, 1, 69, 0)
    threading.Thread(target=_cc, daemon=True).start()


