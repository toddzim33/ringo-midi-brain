def _parse_chaser_data():
    import xml.etree.ElementTree as ET
    try:
        tree = ET.parse(WORKSPACE_PATH)
    except Exception as e:
        log.warning(f"chaser parse failed: {e}")
        return {}
    root = tree.getroot()
    ns = 'http://www.qlcplus.org/Workspace'
    P = f'{{{ns}}}'
    engine = root.find(f'{P}Engine')

    fixture_map = {}
    for fx in engine.findall(f'{P}Fixture'):
        fid  = fx.findtext(f'{P}ID')
        addr = fx.findtext(f'{P}Address')
        if fid and addr:
            fixture_map[int(fid)] = int(addr) + 1  # 1-indexed (Simple Desk)

    fn_map = {int(f.get('ID')): f for f in engine
              if f.tag == f'{P}Function' and f.get('ID')}

    def seq_steps(fn_el):
        all_chs = set()
        raw = []
        for step in sorted(fn_el.findall(f'{P}Step'),
                           key=lambda s: int(s.get('Number', 0))):
            dur = max(int(step.get('FadeIn', 0)) + int(step.get('Hold', 100)) +
                      int(step.get('FadeOut', 0)), 50)
            chs = {}
            if step.text and step.text.strip():
                parts = step.text.strip().split(':')
                i = 0
                while i + 1 < len(parts):
                    base = fixture_map.get(int(parts[i]), 0)
                    pairs = parts[i+1].split(',')
                    for j in range(0, len(pairs)-1, 2):
                        ch = str(base + int(pairs[j]))
                        chs[ch] = int(pairs[j+1])
                        all_chs.add(ch)
                    i += 2
            raw.append((dur, chs))
        return [(d, {ch: chs.get(ch, 0) for ch in all_chs}) for d, chs in raw]

    def resolve(fn_id):
        fn = fn_map.get(fn_id)
        if fn is None:
            return []
        t = fn.get('Type')
        if t == 'Sequence':
            return seq_steps(fn)
        if t == 'Collection':
            children = [resolve(int(s.text.strip()))
                        for s in fn.findall(f'{P}Step') if s.text]
            children = [c for c in children if c]
            if not children:
                return []
            n = max(len(c) for c in children)
            result = []
            for i in range(n):
                dur, chs = 0, {}
                for c in children:
                    if i < len(c):
                        d, ch = c[i]
                        dur = max(dur, d)
                        chs.update(ch)
                result.append((dur, chs))
            return result
        return []  # Scene or other static type -- no animation

    # Discover all VC buttons with animated functions
    note_fn = {}
    for btn in root.iter(f'{P}Button'):
        ic = btn.find(f'{P}Input')
        fe = btn.find(f'{P}Function')
        if ic is not None and fe is not None and fe.get('ID') is not None:
            ch = int(ic.get('Channel', -1))
            note = ch - 144  # MIDI note = input channel - 144
            if 0 <= note <= 110:
                note_fn[note] = int(fe.get('ID'))

    result = {}
    for note, fn_id in note_fn.items():
        steps = resolve(fn_id)
        if steps:
            fn = fn_map.get(fn_id)
            log.info(f"chaser note {note}: "
                     f"{fn.get('Name') if fn is not None else '?'} "
                     f"-> {len(steps)} steps, {sum(d for d,_ in steps)}ms/loop")
            result[note] = steps
    return result


